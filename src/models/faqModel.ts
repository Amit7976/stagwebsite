///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


export interface IFAQ extends Document {
  question: string;
  answer: string;
}


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


// DEFINE THE faq SCHEMA
const FaqSchema = new mongoose.Schema({
    question: {
        type: String,
        default: "",
        require: true
    },
    answer: {
        type: String,
        default: "",
        require: true
    },
}, {
    timestamps: true,
});


